void solve() {
	int n;
	cin >> n;
	vector<vector<char>> a(n, vector<char>(n)), b(n, vector<char>(n));

	for (auto &x : a) {
		for (auto &y : x) {
			cin >> y;
		}
	}

	for (auto &x : b) {
		for (auto &y : x) {
			cin >> y;
		}
	}

	for (int i = 0; i < n; i++) {
		for (int j = 0; j < n; j++) {
			if (a[i][j] != b[i][j]) {
				cout << i + 1 << " " << j + 1 << "\n"; return;
			}
		}
	}
}

